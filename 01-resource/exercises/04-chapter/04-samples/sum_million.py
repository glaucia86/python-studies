# Crie a lista de 1 a 1.000.000 novamente, depois utilize as funções min(), max() e sum() 
# para mostrar o menor valor, o maior valor e a soma total desses números.

numeros = list(range(1, 1000001))
print("Menor valor...: ", min(numeros))
print("Maior valor...: ", max(numeros))
print("Soma total...: ", sum(numeros))