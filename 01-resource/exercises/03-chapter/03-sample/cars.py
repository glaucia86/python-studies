# Classify a list using the sort() method
print("=== Sorting a list... ===")
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)

print()

# Inverse the order of a list using the sort() method
print("=== Sorting a list... ===")
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True)
print(cars)

print()

# Classify a temporary list using the sorted() function
print("=== Sorting a list... ===")
cars = ['bmw', 'audi', 'toyota', 'subaru']
print("Here is the original list...:")
print(cars)

print("Here is the sorted list...:")
print(sorted(cars))

print("Here is the original list again...:")
print(cars)

print()

# Inverse a list using the reverse() method
print("=== Reversing a list... ===")
cars = ['bmw', 'audi', 'toyota', 'subaru']
print("Here is the original list...:")
print(cars)
cars.reverse()
print("Here is the reversed list...:")
print(cars)

print()

# Find the length of a list using the len() function
print("=== Finding the length of a list... ===")
cars = ['bmw', 'audi', 'toyota', 'subaru']
print("Here is the original list...:")
print(cars)
print("The length of the list is:")
print(len(cars))

print()